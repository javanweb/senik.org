exports.seed = async function(knex) {
  // Clean up existing data
  await knex('timeline_items').del();
  await knex('features').del();
  await knex('statistics').del();
  await knex('about_us').del();
  await knex('heroes').del();
  await knex('sites').del();
  await knex('themes').del();

  // Insert themes
  const [theme] = await knex('themes').insert([
    { name: siteContent.theme, styles: JSON.stringify(siteContent.themeColors) },
  ]).returning('*');

  // Insert a site
  const [site] = await knex('sites').insert([
    { site_name: 'سایت سه نیک', domain: 'senik.ir', theme_id: theme.id },
  ]).returning('*');

  // Insert hero content
  await knex('heroes').insert({
    site_id: site.id,
    title: siteContent.hero.title,
    subtitle: siteContent.hero.subtitle,
    background_image: siteContent.hero.imageUrl,
  });

  // Insert about_us content
  const [about] = await knex('about_us').insert({
    site_id: site.id,
    title: siteContent.about.title,
    description: siteContent.about.paragraph,
    list: JSON.stringify([]), // Assuming this is for a list not in the current data
  }).returning('id');

  // Insert timeline_items
  const timelineItems = siteContent.about.timeline.map(item => ({
    about_us_id: about.id,
    year: item.year,
    title: item.description.substring(0, 50), // Use first 50 chars of description as title
    description: item.description,
  }));
  await knex('timeline_items').insert(timelineItems);

  // Insert statistics
  const stats = siteContent.stats;
  const statisticsData = [
    { site_id: site.id, title: stats.experience.label, value: stats.experience.value, icon: 'clock' },
    { site_id: site.id, title: stats.customers.label, value: stats.customers.value, icon: 'users' },
    { site_id: site.id, title: stats.team.label, value: stats.team.value, icon: 'briefcase' },
    { site_id: site.id, title: stats.satisfaction.label, value: stats.satisfaction.value, icon: 'smile' },
  ];
  await knex('statistics').insert(statisticsData);

  // Insert features
  const featuresData = siteContent.features.items.map(item => ({
    site_id: site.id,
    title: item.title,
    description: item.points.join('\n'),
    icon: item.icon,
  }));
  await knex('features').insert(featuresData);
};
